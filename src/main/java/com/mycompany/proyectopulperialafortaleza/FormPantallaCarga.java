// simula la carga de la barra de progreso
    private void startLoading() {
        SwingWorker<Void, Void> worker = new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                for (int i = 0; i <= 100; i++) {
                    Thread.sleep(50); // Simulate some work being done
                    final int progressValue = i;
                    lblbarraprogreso.setText(i+"%");
                    SwingUtilities.invokeLater(() -> barraprogreso.setValue(progressValue));
                }
                
                return null;
            }

            @Override
            protected void done() {
                abrirFormLogin();
            }
        };

        worker.execute();
    }
    
    
    
